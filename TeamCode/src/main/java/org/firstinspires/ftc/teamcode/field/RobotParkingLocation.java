package org.firstinspires.ftc.teamcode.field;

/**
 * Where to park the robot.
 */
public enum RobotParkingLocation {
    /**
     * Park the robot toward the edge (away from the middle of the field)
     */
    EDGE_OF_FIELD,

    /**
     * Park the robot in front of the backdrop after scoring
     */
    IN_FRONT_OF_BACKDROP,

    /**
     * Park the robot in the center (toward the middle of the field)
     */
    CENTER_OF_FIELD
}

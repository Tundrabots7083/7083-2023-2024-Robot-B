package org.firstinspires.ftc.teamcode.field;

/**
 * Location of the team prop
 */
public enum TeamElementLocation {
    /**
     * The location of the team prop is unknown
     */
    UNKNOWN,
    /**
     * The team prop is located on the left spike mark
     */
    LEFT_SPIKE_MARK,
    /**
     * The team prop is located on the middle spike mark
     */
    MIDDLE_SPIKE_MARK,
    /**
     * The team prop is located on the right spike mark
     */
    RIGHT_SPIKE_MARK
}
